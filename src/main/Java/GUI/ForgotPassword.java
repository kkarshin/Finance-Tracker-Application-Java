    // Check username and show security question
    private void checkUsername(){
        String username = jTextFieldUserName.getText();
        if (username.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter username");
            return; // Stop method
        }
        
        String securityQuestion = userManager.getSecurityQuestion(username);
        if (securityQuestion != null){
            // Set the security question in combo box
            jComboBoxSecurityQues.setSelectedItem(securityQuestion); // Show in the combo box
            jComboBoxSecurityQues.setEnabled(false); // Disable changing the question
        } else {
            JOptionPane.showMessageDialog(this, "Username not found");
        }
    }
    
