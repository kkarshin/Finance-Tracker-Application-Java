    private void jButtonResetPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResetPasswordActionPerformed
        try {
            String username = jTextFieldUserName.getText();
            String securityAnswer = jTextFieldSecurityAnswer.getText();
            String newPassword = new String(jPasswordFieldNew.getPassword());
            String confirmPassword = new String(jPasswordFieldConfirm.getPassword());

            // Validate inputs
            if (username.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter username");
                return;
            }
            if (securityAnswer.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter security answer");
                return;
            }
            if (newPassword.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter new password");
                return;
            }
            if (!newPassword.equals(confirmPassword)) {
                JOptionPane.showMessageDialog(this, "Passwords do not match");
                return;
            }

            // Try to reset password
            String result = userManager.resetPassword(username, securityAnswer, newPassword);

            if (result.equals("Password reset successful")) {
                JOptionPane.showMessageDialog(this, "Password reset successful!");
                // Return to login screen
                Login loginForm = new Login();
                loginForm.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, result, "Reset Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            logger.log(Level.SEVERE, "Error during password reset", e);
            JOptionPane.showMessageDialog(this, "An error occurred during password reset");
        }
    }//GEN-LAST:event_jButtonResetPasswordActionPerformed
                        
